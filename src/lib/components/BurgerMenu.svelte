<script>
  import { createEventDispatcher } from "svelte";

  export let isMenuOpen = false;

  const dispatch = createEventDispatcher();
</script>

<button class="w-10 h-10 relative focus:outline-none" on:click={() => dispatch("toggleMenu")}>
  <span class="sr-only">Open main menu</span>
  <div class="block w-5 absolute left-1/2 top-1/2 transform -translate-x-1/2 -translate-y-1/2">
    <!-- Top line -->
    <span
      aria-hidden="true"
      class="block absolute h-0.5 w-5 bg-current transform transition duration-500 ease-in-out"
      class:rotate-45={isMenuOpen}
      class:-translate-y-1.5={!isMenuOpen}
    ></span>
    <!-- Middle line -->
    <span aria-hidden="true" class="block absolute h-0.5 w-5 bg-current transform transition duration-500 ease-in-out" class:opacity-0={isMenuOpen}
    ></span>
    <!-- Bottom line -->
    <span
      aria-hidden="true"
      class="block absolute h-0.5 w-5 bg-current transform transition duration-500 ease-in-out"
      class:-rotate-45={isMenuOpen}
      class:translate-y-1.5={!isMenuOpen}
    ></span>
  </div>
</button>
